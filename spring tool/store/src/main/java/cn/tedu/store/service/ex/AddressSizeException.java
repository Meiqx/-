package cn.tedu.store.service.ex;

public class AddressSizeException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8511369118726248990L;

	public AddressSizeException() {
		super();
		
	}

	public AddressSizeException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public AddressSizeException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public AddressSizeException(String message) {
		super(message);
		
	}

	public AddressSizeException(Throwable cause) {
		super(cause);
		
	}
	
	
}
